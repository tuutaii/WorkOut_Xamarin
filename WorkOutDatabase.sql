CREATE PROC PROC_GetBaiTapChoNguoiMoi
AS 
SELECT * FROM BAITAP, DOITUONG WHERE BAITAP.TENDOITUONG = DOITUONG.TENDOITUONG AND ID_DOITUONG = 1


CREATE PROC PROC_GetAllBaiTapTrungBinh
AS 
SELECT * FROM BAITAP where TENDOITUONG = 'TRUNG BÌNH'


CREATE PROC PROC_GetAllBaiTapNangCao
AS 
