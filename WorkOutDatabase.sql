CREATE DATABASE WORKOUTDATABASE

CREATE TABLE DANHMUC(
	ID_DANHMUC INT NOT NULL,
	TENDANHMUC NVARCHAR(50) NOT NULL,
	MOTADANHMUC NVARCHAR(200) NOT NULL,
	CONSTRAINT PK_DANHMUC PRIMARY KEY (TENDANHMUC)
)

CREATE TABLE DOITUONG(
	ID_DOITUONG INT NOT NULL,
	TENDOITUONG NVARCHAR(50) NOT NULL,
	MOTADOITUONG NVARCHAR(200),
	CONSTRAINT PK_DOITUONG PRIMARY KEY (TENDOITUONG )
)
CREATE TABLE HINHANH(
	ID INT NOT NULL,
	HINHANH NVARCHAR(MAX) NOT NULL
)


CREATE TABLE BAITAP(
	ID_BAITAP INT NOT NULL PRIMARY KEY,
	TENBAITAP NVARCHAR(100) NOT NULL,
	TENDANHMUC NVARCHAR(50) NOT NULL,
	TENDOITUONG NVARCHAR(50) NOT NULL,
	HINHANH NVARCHAR(MAX) NOT NULL,
	MOTABAITAP NVARCHAR(MAX) NOT NULL,
	THOILUONG INT,
	LUONGCALOTIEUTHU INT,
	TACDUNG NVARCHAR(200),
	CONSTRAINT FK_TENDANHMUC_BT FOREIGN KEY (TENDANHMUC) REFERENCES DANHMUC (TENDANHMUC),
	CONSTRAINT FK_TENDOITUONG_BT FOREIGN KEY (TENDOITUONG) REFERENCES DOITUONG (TENDOITUONG),
)

